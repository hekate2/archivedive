CREATE TABLE "sites" (
	"id"	INTEGER NOT NULL,
	"title"	TEXT NOT NULL,
	"url"	TEXT NOT NULL,
	"summary"	TEXT,
	"tags"	NUMERIC NOT NULL,
	"archived_on"	DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
	"visits"	INTEGER NOT NULL DEFAULT 0,
	"display_count"	INTEGER NOT NULL DEFAULT 0,
	PRIMARY KEY("id" AUTOINCREMENT)
);